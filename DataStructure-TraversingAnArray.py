# Traversing all the given Elements of the Array .
# we will access all the given elements in an array and will print them one by one .

from array import *
#Creating an Array.

my_array1 = array("i",[10,20,30,40,50])

for i in my_array1:
    print(f"The Elemnent is {i}")


# Time Complexity  for traversing the Given Array is: O(n)
# Space Complexity for traversing the Given Array is : O(1)