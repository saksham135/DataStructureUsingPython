#Linear Time Complexity
# In Linear Time Complexity with the increase in Number of Elements  the Number of Operations also increases
# Time Complexity increases will grow directly with proportional to the increase with Number of Elements .

def print_me(n):
    for i in range(n):
        print(i)


print_me(100)

#Time Complexity is denoted as O(n).

